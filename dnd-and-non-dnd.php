<p> DND and non-DND refer to a mobile number's status in India, in relation to the National Do Not Call Registry or Do Not Disturb service.


<b>DND (Do Not Disturb):</b>

<p>DND numbers are those that have been registered on the National Do Not Call Registry, or who have activated DND service on their mobile phones.
If a mobile phone number is listed on the DND list it means the owner has asked not to be contacted by unsolicited promotional messages or telemarketing calls.
In India, there are several categories of DND. These include fully blocked (no promotion messages), partially block (selected categories allowed), and others.
Non-DND (Non-Do Not Disturb):</p>

<p>Non-DND numbers are those that have not been registered in the National Do Not Call Registry and whose owner has not activated DND on their phone.
Businesses and service providers can send promotional messages and make calls to non-DND numbers.</p>

<p>The Telecom Regulatory Authority of India introduced DND in India to give mobile phone users more control over promotional calls and messages they receive. Telemarketers must respect the preferences of users who activate DND to block unwanted communications. Telemarketers must check the National Customer Preference Register, which includes a list DND numbers before sending promotional messages and making marketing calls.</p>

