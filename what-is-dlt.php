<p>The most popular form of DLT is . Each block contains a series of transactions. The blocks are linked together cryptographically to ensure that once a transaction has been recorded, it is nearly impossible to change. Bitcoin and Ethereum, two examples of cryptocurrency built on the blockchain technology.
Beyond blockchain: The DLT is more than just the blockchain.</p>

<p>Directed Acyclic graphs (DAGs), and Hashgraph are other types of DLT. These technologies are suitable for a variety of use cases because they offer different consensus mechanisms, scalability and other features.
Applications The DLT is used in a variety of ways. It's used in the finance industry for a secure and transparent settlement of transactions, in supply chain management for tracking the origin and movements of goods, in healthcare for managing patient data, and in voting systems for ensuring the integrity of elections.
