    function showPage(page) {
      const start = (page - 1) * postsPerPage;
      const end = start + postsPerPage;
      const postsToDisplay = posts.slice(start, end);

      const container = document.getElementById('blog-posts-container');
      container.innerHTML = ''; // Clear current posts

      postsToDisplay.forEach(post => {
        const postHTML = `
          <div class="col-sm-6 col-lg-4 wow fadeInLeft">
            <article class="post post-modern">
              <a class="post-modern-figure" href="${post.link}">
                <img src="${post.image}" alt="blog-img" width="370" height="307" />
              </a>
              <h5 class="post-modern-title"><a href="${post.link}">${post.title}</a></h5>
              <p class="post-modern-text">${post.description}</p>
            </article>
          </div>
        `;
        container.innerHTML += postHTML;
      });

      // Update pagination
      updatePagination(page);
    }

    // Function to update pagination buttons
    function updatePagination(page) {
      const totalPages = Math.ceil(posts.length / postsPerPage);
      const paginationContainer = document.getElementById('pagination');
      paginationContainer.innerHTML = ''; // Clear pagination

      for (let i = 1; i <= totalPages; i++) {
        const button = document.createElement('button');
        button.innerText = i;
        button.classList.add('pagination-button');
        if (i === page) button.classList.add('active');
        button.addEventListener('click', () => showPage(i));
        paginationContainer.appendChild(button);
      }
    }

    // Initialize pagination
    showPage(1);
